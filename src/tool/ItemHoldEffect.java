	public boolean isTypeBoosting(Type type) {
		return (this == BUG_POWER || this == STRENGTHEN_BUG || this == ARCEUS_BUG) && type == Type.BUG
			|| (this == STEEL_POWER || this == STRENGTHEN_STEEL || this == ARCEUS_STEEL) && type == Type.STEEL
			|| (this == GROUND_POWER || this == STRENGTHEN_GROUND || this == ARCEUS_GROUND) && type == Type.GROUND
			|| (this == ROCK_POWER || this == STRENGTHEN_ROCK || this == ARCEUS_ROCK) && type == Type.ROCK
			|| (this == GRASS_POWER || this == STRENGTHEN_GRASS || this == ARCEUS_GRASS) && type == Type.GRASS
			|| (this == DARK_POWER || this == STRENGTHEN_DARK || this == ARCEUS_DARK) && type == Type.DARK
			|| (this == FIGHTING_POWER || this == STRENGTHEN_FIGHT || this == ARCEUS_FIGHT) && type == Type.FIGHTING
			|| (this == ELECTRIC_POWER || this == STRENGTHEN_ELECTRIC || this == ARCEUS_ELECTRIC) && type == Type.ELECTRIC
			|| (this == WATER_POWER || this == STRENGTHEN_WATER || this == ARCEUS_WATER) && type == Type.WATER
			|| (this == FLYING_POWER || this == STRENGTHEN_FLYING || this == ARCEUS_FLYING) && type == Type.FLYING
			|| (this == POISON_POWER || this == STRENGTHEN_POISON || this == ARCEUS_POISON) && type == Type.POISON
			|| (this == ICE_POWER || this == STRENGTHEN_ICE || this == ARCEUS_ICE) && type == Type.ICE
			|| (this == GHOST_POWER || this == STRENGTHEN_GHOST || this == ARCEUS_GHOST) && type == Type.GHOST
			|| (this == PSYCHIC_POWER || this == STRENGTHEN_PSYCHIC || this == ARCEUS_PSYCHIC) && type == Type.PSYCHIC
			|| (this == FIRE_POWER || this == STRENGTHEN_FIRE || this == ARCEUS_FIRE) && type == Type.FIRE
			|| (this == DRAGON_POWER || this == STRENGTHEN_DRAGON || this == ARCEUS_DRAGON) && type == Type.DRAGON
			|| (this == NORMAL_POWER || this == STRENGTHEN_NORMAL) && type == Type.NORMAL;
	}
	

	public int getCritStage(Species s){
		if(this == SCOPE_LENS || this == CRITRATE_UP)
			return 1;
		else if (s == Species.getSpeciesByName("FARFETCHD") && (this == STICK || this == FARFETCHD_CRITRATE_UP)
		      ||(s == Species.getSpeciesByName("CHANSEY") && (this == LUCKY_PUNCH || this == CHANSEY_CRITRATE_UP))
		      || this == CRITICAL_UP_2)
			return 2;
		else 
			return 0;
	}
	
	public boolean isMoneyBoosting() {
		return this == ItemHoldEffect.DOUBLE_PRIZE || this == ItemHoldEffect.MONEY_UP;
	}
	
	public boolean isBoostingHappiness() {
		return this == HAPPINESS_UP ||
				this == FRIENDSHIP_UP;
	}
